class Documents extends PureComponent {
  constructor(props) {
    super(props);
    this.state = {
      isViewingDocument: false,
      selectedFile: [],
      typeOfSelectedFile: '',
    };
  }

  onBackClick = () => {
    this.setState({
      isViewingDocument: false,
    });
  };

  onFileClick = (value) => {
    console.log('file: ', value);
    // eslint-disable-next-line array-callback-return
    data.some((x) => {
      // eslint-disable-next-line array-callback-return
      x.body.some((y) => {
        // eslint-disable-next-line array-callback-return
        y.files.some((z) => {
          if (z.id === value) {
            this.setState({
              isViewingDocument: true,
              selectedFile: z,
              typeOfSelectedFile: y.kind,
            });
            return true;
          }
          return false;
        });
      });
    });
  };

  render() {
    const { isViewingDocument, selectedFile, typeOfSelectedFile } = this.state;
    return (
      <div className={styles.Documents}>
        {isViewingDocument ? (
          <ViewDocument
            selectedFile={selectedFile}
            typeOfSelectedFile={typeOfSelectedFile}
            onBackClick={this.onBackClick}
          />
        ) : (
          data.map((value) => <InfoCollapseType2 onFileClick={this.onFileClick} data={value} />)
        )}
      </div>
    );
  }
}

export default Documents;
